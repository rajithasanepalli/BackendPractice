public class Incdec {

	public static void main(String[] args) {
	//create variables
		int a=8,b=3;
		//increment and decrements
		System.out.println("enter values of a and b\t"+(++a)+"\t"+(b++));
		System.out.println("enter values of a and b\t"+(a++)+"\t"+b);
		System.out.println("enter values of a and b\t"+a+"\t"+(b++));
		System.out.println("enter values of a and b\t"+b+"\t"+a);
		System.out.println("enter values of a and b\t"+(b--)+"\t"+(++a));
		System.out.println("enter the values of a and b\t"+(--a)+"\t"+(b));
		System.out.println("enter the values of and b\t"+(a--)+"\t"+(b--));
		
		

	}

}
