


public class Swapping {

	public static void main(String[] args) {
		// creating variables
	int a=6,b=3,temp;
	//before swapping
	System.out.println("before swapping the values of a and b is \t a="+a+"\tb="+b);
	//after swapping
	temp=a;
	a=b;
	b=temp;
	System.out.println("after swapping the values of a and b is \ta="+a+"\tb="+b);
	

	}

}
